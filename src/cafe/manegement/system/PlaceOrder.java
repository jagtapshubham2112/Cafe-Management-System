    public void validateFields(){
        String customerName = txtCustomerName.getText();
        String customerMobileNumber = txtCustomerMobileNumber.getText();
        String customerEmail = txtCustomerEmailAddress.getText();
        if(!customerEmail.equals("") && customerMobileNumber.matches(moileNumberPattern) && customerMobileNumber.length() == 10 && customerEmail.matches(emaliPattern) && grandTotal > 0){
            btnGenerateTotalBillAndPrint.setEnabled(true);
        }
        else{
            btnGenerateTotalBillAndPrint.setEnabled(false);
        }
    }

